  INICIO_APLICATIVO,
  INSTITUCIONAL,
  BIBLIA,
  REALIZAR_INSCRICAO_EBD,
  CONSULTAR_PLANOS_LEITURA_BIBLICA,
  LISTAR_ESTUDOS,
  YOUTUBE,
  LISTAR_BOLETINS,
  LISTAR_PUBLICACOES,
  CONSULTAR_HINARIO,
  CONSULTAR_CIFRAS,
  CONSULTAR_CANTICOS,
  PEDIR_ORACAO,
  PREFERENCIAS,
  NOTIFICACOES,
