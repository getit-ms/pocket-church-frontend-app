  ANIVERSARIANTES,
  REALIZAR_VOTACAO,
  GALERIA_FOTOS,
  AUDIOS,


}

Funcionalidade FuncionalidadeByName(String name) {
  return Funcionalidade.values
      .firstWhere((fnc) => fnc.toString() == "Funcionalidade.$name",
      orElse: () => null);
}